function setLanguage(lang) {
    const t = translations[lang];
    document.documentElement.lang = lang;
    
    const setSafeText = (selector, text) => {
        const el = document.querySelector(selector);
        if (el) el.textContent = text;
    };
    const setSafeHTML = (selector, html) => {
        const el = document.querySelector(selector);
        if (el) el.innerHTML = html;
    };

    setSafeText('#lang-indicator', lang.toUpperCase());

    // Nav
    setSafeText('[data-i18n="navAbout"]', t.navAbout);
    setSafeText('[data-i18n="navSkills"]', t.navSkills);
    setSafeText('[data-i18n="navTrajectory"]', t.navTrajectory);
    setSafeText('[data-i18n="navProjects"]', t.navProjects);
    setSafeText('[data-i18n="navContact"]', t.navContact);
    setSafeText('[data-i18n="downloadCV"]', t.downloadCV);

    // Hero
    setSafeText('[data-i18n="availableTag"]', t.availableTag);
    setSafeHTML('[data-i18n="heroGreeting"]', t.heroGreeting + ' <br/><span class="text-transparent bg-clip-text bg-gradient-to-r from-primary to-blue-400">' + t.heroRole + '</span>');
    setSafeHTML('[data-i18n="heroDesc"]', t.heroDesc);
    setSafeText('[data-i18n="viewProjects"]', t.viewProjects);
    setSafeText('[data-i18n="contactMe"]', t.contactMe);

    // About
    setSafeText('[data-i18n="aboutTitle"]', t.aboutTitle);
    setSafeText('[data-i18n="aboutP1"]', t.aboutP1);
    setSafeText('[data-i18n="aboutP2"]', t.aboutP2);
    setSafeText('[data-i18n="aboutP3"]', t.aboutP3);
    setSafeText('[data-i18n="aboutP4"]', t.aboutP4);

    // Skills
    setSafeText('[data-i18n="skillsTitle"]', t.skillsTitle);
    setSafeText('[data-i18n="skillsSubtitle"]', t.skillsSubtitle);
    setSafeText('[data-i18n="skillsFrontend"]', t.skillsFrontend);
    setSafeText('[data-i18n="skillsBackend"]', t.skillsBackend);
    setSafeText('[data-i18n="skillsDatabase"]', t.skillsDatabase);
    setSafeText('[data-i18n="skillsTools"]', t.skillsTools);

    // Trajectory
    setSafeText('[data-i18n="trajectoryTitle"]', t.trajectoryTitle);
    setSafeText('[data-i18n="trajectorySubtitle"]', t.trajectorySubtitle);
    setSafeText('[data-i18n="experienceTitle"]', t.experienceTitle);
    setSafeText('[data-i18n="educationTitle"]', t.educationTitle);
    setSafeText('[data-i18n="jobTitle"]', t.jobTitle);
    setSafeText('[data-i18n="jobCompany"]', t.jobCompany);
    setSafeText('[data-i18n="jobCurrently"]', t.jobCurrently);
    setSafeText('[data-i18n="jobDesc1"]', t.jobDesc1);
    setSafeText('[data-i18n="jobDesc2"]', t.jobDesc2);
    setSafeText('[data-i18n="jobDesc3"]', t.jobDesc3);
    setSafeText('[data-i18n="jobDesc4"]', t.jobDesc4);
    setSafeText('[data-i18n="eduTitle1"]', t.eduTitle1);
    setSafeText('[data-i18n="eduInstitution1"]', t.eduInstitution1);
    setSafeText('[data-i18n="eduDesc1"]', t.eduDesc1);
    setSafeText('[data-i18n="eduTitle2"]', t.eduTitle2);
    setSafeText('[data-i18n="eduInstitution2"]', t.eduInstitution2);
    setSafeText('[data-i18n="eduDesc2"]', t.eduDesc2);

    // Projects
    setSafeText('[data-i18n="projectsTitle"]', t.projectsTitle);
    setSafeText('[data-i18n="projectsPageTitle"]', t.projectsPageTitle || t.projectsTitle);
    setSafeText('[data-i18n="projectsSubtitle"]', t.projectsSubtitle);
    setSafeText('[data-i18n="backToHome"]', t.backToHome || (lang === 'en' ? 'Back to home' : 'Volver al inicio'));
    
    if (document.getElementById('projects-grid')) {
        renderProjects(lang, t.preview);
    }

    // Contact
    setSafeText('[data-i18n="contactTitle"]', t.contactTitle);
    setSafeText('[data-i18n="contactDesc"]', t.contactDesc);
    setSafeText('[data-i18n="contactEmail"]', t.contactEmail);
    setSafeText('[data-i18n="contactLocation"]', t.contactLocation);
    setSafeText('[data-i18n="contactSocial"]', t.contactSocial);
    setSafeText('[data-i18n="formName"]', t.formName);
    setSafeText('[data-i18n="formEmail"]', t.formEmail);
    setSafeText('[data-i18n="formSubject"]', t.formSubject);
    
    const subjectOptions = document.querySelectorAll('#subject option');
    if (subjectOptions.length >= 4) {
        subjectOptions[0].textContent = t.formSubjectOpt1 || 'Inquietud general';
        subjectOptions[1].textContent = t.formSubjectOpt2 || 'Propuesta de proyecto';
        subjectOptions[2].textContent = t.formSubjectOpt3 || 'Trabajo Freelance';
        subjectOptions[3].textContent = t.formSubjectOpt4 || 'Otro';
    }
    
    setSafeText('[data-i18n="formMessage"]', t.formMessage);
    const msgEl = document.getElementById('message');
    if (msgEl) msgEl.placeholder = t.formMessagePlaceholder || '...';
    setSafeText('[data-i18n="formSubmit"]', t.formSubmit);

    // Footer
    setSafeText('[data-i18n="footerDesigned"]', t.footerDesigned);
    setSafeText('[data-i18n="footerRights"]', t.footerRights);

    localStorage.setItem('lang', lang);
}

document.getElementById('lang-toggle').addEventListener('click', function () {
    const currentLang = localStorage.getItem('lang') || 'es';
    const newLang = currentLang === 'es' ? 'en' : 'es';
    setLanguage(newLang);
});

// Initialize language from saved preference
(function () {
    const savedLang = localStorage.getItem('lang') || 'es';
    setLanguage(savedLang);
})();
